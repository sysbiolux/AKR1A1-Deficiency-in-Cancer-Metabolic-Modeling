end


function [pathways,scores] = prepare_FVA( ...
    pathways,scores,nRequested)
%PREPARE_FVA Sort scores and retain the requested number of pathways.

[scores,idx] = sort(scores,'ascend');
pathways = pathways(idx);

n = min(nRequested,numel(scores));

pathways = pathways(1:n);
scores = scores(1:n);

end


function draw_cell_grid(ax,nRows)
%DRAW_CELL_GRID Add borders around heatmap cells.

hold(ax,'on');

for y = 0.5:1:(nRows + 0.5)

    line(ax,[0.5 1.5],[y y], ...
        'Color',[0.45 0.45 0.45], ...
        'LineWidth',0.25);
end

line(ax,[0.5 0.5],[0.5 nRows + 0.5], ...
    'Color',[0.35 0.35 0.35], ...
    'LineWidth',0.4);

line(ax,[1.5 1.5],[0.5 nRows + 0.5], ...
    'Color',[0.35 0.35 0.35], ...
    'LineWidth',0.4);

hold(ax,'off');

end


function [pathways,scores] = read_FVA(filename)
%READ_FVA Read pathway names and FVA similarity scores from Excel.
%
% Expected file organization:
%
%   First row:
%       column headers
%
%   Pathway column:
%       Pathways, Pathway, subSys, or Subsystem
%
%   Score column:
%       column 2
%
% MATLAB R2019b compatible.

if exist(filename,'file') ~= 2
    error('Input file not found: %s',filename);
end

[~,~,raw] = xlsread(filename);

if isempty(raw)
    error('The file is empty or could not be read: %s',filename);
end

if size(raw,2) < 2
    error('The file must contain at least two columns: %s',filename);
end

header = raw(1,:);
pathwayCol = [];

for j = 1:numel(header)

    if isempty(header{j})
        continue
    end

    headerName = strtrim(char(string(header{j})));

    if strcmpi(headerName,'Pathways') || ...
       strcmpi(headerName,'Pathway') || ...
       strcmpi(headerName,'subSys') || ...
       strcmpi(headerName,'Subsystem')

        pathwayCol = j;
        break
    end
end

if isempty(pathwayCol)
    pathwayCol = 1;
end

scoreCol = 2;

if pathwayCol == scoreCol
    error(['The pathway column and score column are both column 2. ' ...
           'Check the Excel file: %s'],filename);
end

pathwayCells = raw(2:end,pathwayCol);
scoreCells = raw(2:end,scoreCol);

pathways = strings(numel(pathwayCells),1);
scores = nan(numel(scoreCells),1);

for r = 1:numel(pathwayCells)

    pathwayValue = pathwayCells{r};

    if isempty(pathwayValue)
        pathways(r) = "";
    else
        pathways(r) = strtrim(string(pathwayValue));
    end

    scoreValue = scoreCells{r};

    if isnumeric(scoreValue) && isscalar(scoreValue)

        scores(r) = double(scoreValue);

    elseif ischar(scoreValue) || isstring(scoreValue)

        scores(r) = str2double(string(scoreValue));

    else

        scores(r) = NaN;
    end
end

valid = ...
    isfinite(scores) & ...
    strlength(strtrim(pathways)) > 0;

pathways = pathways(valid);
scores = scores(valid);

if isempty(scores)
    error('No valid pathway scores were found in: %s',filename);
end

end