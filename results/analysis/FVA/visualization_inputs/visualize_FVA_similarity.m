        'Parent',fig, ...
        'Position',[0.57,0.12,0.10,0.80]);

    imagesc(ax,scores(:));

    colormap(ax,whiteRed);
    caxis(ax,colorLimits);

    set(ax, ...
        'YTick',1:numel(scores), ...
        'YTickLabel',cellstr(pathways), ...
        'XTick',1, ...
        'XTickLabel',labels{i}, ...
        'XTickLabelRotation',0, ...
        'TickLabelInterpreter','none', ...
        'FontSize',8, ...
        'Box','on', ...
        'LineWidth',0.5);

    title(ax,'FVA similarity', ...
        'FontSize',10, ...
        'FontWeight','bold');

    draw_cell_grid(ax,numel(scores));

    cb = colorbar(ax);

    set(cb, ...
        'Position',[0.72,0.12,0.045,0.80], ...
